package com.heheys.ec.controller.listener;

/**
 * 
 * Describe:滑动回调接口
 * 
 * Date:2015-9-29
 * 
 * Author:liuzhouliang
 */
public interface IScrollListener {
	/**
	 * 滚动加载数据到底部了
	 */
	public void onScrollToBottom();

	/**
	 * 滚动加载数据到最后一页了(用于加载到最后一页Toast提示处理)
	 */
	public void onScrollToLastPage();
}
